#!/usr/bin/env node

/**
 * Builds portable agent skill artifacts from the canonical @n8n-as-code/skills
 * source and mirrors them into facade plugin trees.
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { execFileSync } from 'child_process';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const WORKSPACE_ROOT = path.resolve(__dirname, '..', '..', '..');
const PACKAGE_ROOT = path.resolve(__dirname, '..');
const DIST_DIR = path.join(PACKAGE_ROOT, 'dist', 'adapters', 'agent-skills');
const ROOT_SKILLS_DIR = path.join(WORKSPACE_ROOT, 'skills');
const SKILL_NAMES = ['n8n-manager', 'n8n-architect'];
const PLUGIN_SKILL_ROOTS = [
  path.join(WORKSPACE_ROOT, 'plugins', 'claude', 'n8n-as-code', 'skills'),
  path.join(WORKSPACE_ROOT, 'plugins', 'openclaw', 'n8n-as-code', 'skills'),
  path.join(WORKSPACE_ROOT, 'plugins', 'cursor', 'n8n-as-code', 'skills'),
];
const MIRROR_SKILL_ROOTS = [
  ROOT_SKILLS_DIR,
];
const checkMode = process.argv.includes('--check');

const colors = {
  reset: '\x1b[0m',
  green: '\x1b[32m',
  blue: '\x1b[34m',
  yellow: '\x1b[33m',
  cyan: '\x1b[36m',
  gray: '\x1b[90m',
  red: '\x1b[31m',
};

function log(message, color = 'reset') {
  console.log(`${colors[color]}${message}${colors.reset}`);
}

async function getAiContextGenerator() {
  const distPath = path.resolve(__dirname, '..', 'dist', 'services', 'ai-context-generator.js');
  if (!fs.existsSync(distPath)) {
    throw new Error('AiContextGenerator not found in dist/. Please run "npm run build --workspace=packages/skills" first.');
  }
  const mod = await import(distPath);
  return new mod.AiContextGenerator();
}

function writeSkill(root, skillName, content) {
  const skillDir = path.join(root, skillName);
  fs.mkdirSync(skillDir, { recursive: true });
  fs.writeFileSync(path.join(skillDir, 'SKILL.md'), content);
}

function readIfExists(filePath) {
  return fs.existsSync(filePath) ? fs.readFileSync(filePath, 'utf8') : null;
}

function assertFileContent(filePath, expected) {
  const existing = readIfExists(filePath);
  if (existing !== expected) {
    throw new Error(`Generated skill mirror is out of date: ${path.relative(WORKSPACE_ROOT, filePath)}`);
  }
}

function writeOrCheckSkill(root, skillName, content) {
  const skillPath = path.join(root, skillName, 'SKILL.md');
  if (checkMode) {
    assertFileContent(skillPath, content);
    return;
  }
  writeSkill(root, skillName, content);
}

function resolveAdapterDistTag() {
  const explicit = process.env.N8NAC_SKILL_ADAPTER_DIST_TAG?.trim();
  if (explicit) {
    return ['stable', 'latest', 'none'].includes(explicit) ? undefined : explicit;
  }

  // Release PRs targeting main should validate the stable payload, even when
  // the source branch is named next.
  if (process.env.GITHUB_BASE_REF === 'main') {
    return undefined;
  }

  const packageVersions = [
    path.join(WORKSPACE_ROOT, 'packages', 'cli', 'package.json'),
    path.join(WORKSPACE_ROOT, 'packages', 'skills', 'package.json'),
  ]
    .map(readPackageVersion)
    .filter(Boolean);

  if (packageVersions.some((version) => version.includes('-next'))) {
    return 'next';
  }

  const branch = process.env.GITHUB_REF_NAME || process.env.BRANCH_NAME || readGitBranch();
  return branch === 'next' ? 'next' : undefined;
}

function readPackageVersion(packagePath) {
  try {
    return JSON.parse(fs.readFileSync(packagePath, 'utf8')).version;
  } catch {
    return undefined;
  }
}

function readGitBranch() {
  try {
    return execFileSync('git', ['branch', '--show-current'], {
      cwd: WORKSPACE_ROOT,
      encoding: 'utf8',
      stdio: ['ignore', 'pipe', 'ignore'],
    }).trim();
  } catch {
    return undefined;
  }
}

function cleanDist() {
  if (checkMode) return;
  log('\nCleaning generated skill adapter dist...', 'blue');
  fs.rmSync(DIST_DIR, { recursive: true, force: true });
  fs.mkdirSync(DIST_DIR, { recursive: true });
}

function generateReadme() {
  const readme = `# n8n-as-code Agent Skills

Portable n8n-as-code skills for agents that can read \`SKILL.md\` files.

## Skills

- \`n8n-manager\`: instance, runtime, tunnel, auth, project, credential, and workflow presentation management.
- \`n8n-architect\`: workflow authoring, sync, node schemas, validation, push/pull, and context-root workflow operations.

Install the skills in your agent runtime from this repository directory:

\`\`\`
https://github.com/EtienneLescot/n8n-as-code/tree/main/skills
\`\`\`

This directory contains both required skills:

\`\`\`
skills/n8n-manager/SKILL.md
skills/n8n-architect/SKILL.md
\`\`\`

Generic skill installers can discover the \`skills/*/SKILL.md\` directories. Installers that require an explicit subpath can use:

\`\`\`
skills/n8n-manager
skills/n8n-architect
\`\`\`

Workspace-local copies generated by \`n8nac update-ai\` live at:

\`\`\`
.agents/skills/n8n-manager/SKILL.md
.agents/skills/n8n-architect/SKILL.md
\`\`\`
`;
  const distReadmePath = path.join(DIST_DIR, 'README.md');
  const rootReadmePath = path.join(ROOT_SKILLS_DIR, 'README.md');
  if (checkMode) {
    assertFileContent(distReadmePath, readme);
    assertFileContent(rootReadmePath, readme);
    return;
  }
  fs.mkdirSync(ROOT_SKILLS_DIR, { recursive: true });
  fs.writeFileSync(distReadmePath, readme);
  fs.writeFileSync(rootReadmePath, readme);
}

async function generateSkills() {
  const generator = await getAiContextGenerator();
  const distTag = resolveAdapterDistTag();

  for (const skillName of SKILL_NAMES) {
    const content = generator.getAgentSkillContent(skillName, distTag);
    writeOrCheckSkill(DIST_DIR, skillName, content);
    for (const mirrorSkillRoot of MIRROR_SKILL_ROOTS) {
      writeOrCheckSkill(mirrorSkillRoot, skillName, content);
    }
    for (const pluginSkillRoot of PLUGIN_SKILL_ROOTS) {
      writeOrCheckSkill(pluginSkillRoot, skillName, content);
    }
  }
}

function printSummary() {
  log('\n' + '='.repeat(60), 'cyan');
  log('Agent skill adapter build complete.', 'green');
  log('='.repeat(60), 'cyan');
  log(`\nGenerated dist: ${DIST_DIR}`, 'yellow');
  log(`Command channel: ${resolveAdapterDistTag() || 'stable'}`, 'yellow');
  log(`Root installable skills: ${ROOT_SKILLS_DIR}`, 'yellow');
  for (const pluginRoot of PLUGIN_SKILL_ROOTS) {
    log(`Mirrored plugin skills: ${pluginRoot}`, 'yellow');
  }
}

(async () => {
  try {
    log(checkMode ? '\nChecking portable agent skill adapters...' : '\nBuilding portable agent skill adapters...', 'blue');
    log(`Root: ${PACKAGE_ROOT}`, 'gray');
    cleanDist();
    await generateSkills();
    generateReadme();
    if (checkMode) {
      log('\nAll generated skill mirrors are up to date.', 'green');
    } else {
      printSummary();
    }
  } catch (error) {
    log('\nSkill adapter build failed:', 'red');
    console.error(error);
    process.exit(1);
  }
})();
